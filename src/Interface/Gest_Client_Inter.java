    public void afficherClients() {
        Gest_Client gc = new Gest_Client();
        ArrayList<Client> c = gc.AfficheClients();

        DefaultTableModel model = (DefaultTableModel) tblClients.getModel();

        Object[] data = new Object[7];

        model.setRowCount(0);
        for (int i = 0; i < c.size(); i++) {

            data[0] = c.get(i).getCin();
            data[1] = c.get(i).getNom();
            data[2] = c.get(i).getPrenom();
            data[3] = c.get(i).getDateNaiss();
            data[4] = c.get(i).getNumTelephone();
            data[5] = c.get(i).getAdresse();
            data[6] = c.get(i).getSexe();

            model.addRow(data);
        }

        lblNbClient.setText("Nombre des clients : " + c.size());

    }


    private void btnAjoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjoutActionPerformed

        JFrame frame = new JFrame("Message");
        if (txtCin.getText().length() == 8 || txtCin.getText().equals("") || txtNom.getText().equals("") || txtPrenom.getText().equals("") || txtTelphon.getText().equals("") || txtAdresse.getText().equals("")) {

            try {

                Client client = new Client(txtCin.getText(), txtNom.getText().toUpperCase(), txtPrenom.getText(), dteNaiss.getDate(), txtTelphon.getText(), txtAdresse.getText(), buttonGroup1.getSelection().getActionCommand());

                Gest_Client gc = new Gest_Client();
                gc.AjouterClient(client);

                DefaultTableModel model = (DefaultTableModel) tblClients.getModel();
                model.setRowCount(0);

                afficherClients();

                txtCin.setText("");
                txtNom.setText("");
                txtPrenom.setText("");
                txtTelphon.setText("");
                txtAdresse.setText("");
                dteNaiss.setDate(null);

                JOptionPane.showMessageDialog(frame, client.getNom() + " " + client.getPrenom() + " est ajouté", "Succés", JOptionPane.INFORMATION_MESSAGE);
                Gest_Compte_Inter compte_Inter = new Gest_Compte_Inter(client);
                compte_Inter.setVisible(true);
                dispose();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(frame, "Probleme d'ajout", "Erreur", JOptionPane.ERROR_MESSAGE);
            }

        } else {

            JOptionPane.showMessageDialog(frame, "Les champs invalide", "Erreur ", JOptionPane.ERROR_MESSAGE);

        }


    }//GEN-LAST:event_btnAjoutActionPerformed

    private void tblClientsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientsMouseClicked
        btnSupp.setEnabled(true);
        btnMod.setEnabled(true);

        DefaultTableModel model = (DefaultTableModel) tblClients.getModel();

        Date date;
        try {
            date = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(tblClients.getSelectedRow(), 3).toString());
            dteNaiss.setDate(date);
        } catch (ParseException ex) {
            Logger.getLogger(Gest_Client_Inter.class.getName()).log(Level.SEVERE, null, ex);
        }

        txtCin.setEnabled(false);

        txtCin.setText(model.getValueAt(tblClients.getSelectedRow(), 0).toString());
        txtNom.setText(model.getValueAt(tblClients.getSelectedRow(), 1).toString());
        txtPrenom.setText(model.getValueAt(tblClients.getSelectedRow(), 2).toString());
        txtTelphon.setText(model.getValueAt(tblClients.getSelectedRow(), 4).toString());
        txtAdresse.setText(model.getValueAt(tblClients.getSelectedRow(), 5).toString());


    }//GEN-LAST:event_tblClientsMouseClicked

    private void btnSuppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuppActionPerformed

        JFrame frame = new JFrame("Message");

        try {

            Client client = new Client(txtCin.getText(), txtNom.getText().toUpperCase(), txtPrenom.getText(), new java.sql.Date(12), txtTelphon.getText(), txtAdresse.getText(), buttonGroup1.getSelection().getActionCommand());

            Gest_Client gc = new Gest_Client();

            int rep = JOptionPane.showConfirmDialog(null, "Voulez-vous vraiment supprimer " + txtNom.getText() + " " + txtPrenom.getText(), "", JOptionPane.YES_NO_CANCEL_OPTION);

            if (rep == 0) {
                gc.SupprimerClient(client);
                afficherClients();

                txtCin.setText("");
                txtNom.setText("");
                txtPrenom.setText("");
                txtTelphon.setText("");
                txtAdresse.setText("");
                txtCin.setEnabled(true);
                btnSupp.setEnabled(false);
                btnMod.setEnabled(false);

                JOptionPane.showMessageDialog(frame, client.getNom() + " " + client.getPrenom() + " est supprimé", "Succés", JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(frame, "Probleme de suppression", "Erreur", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnSuppActionPerformed


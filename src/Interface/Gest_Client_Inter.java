    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        if (!txtSearch.getText().equals("")) {

            Gest_Client gc = new Gest_Client();
            Client c = gc.AfficheClient(txtSearch.getText());

            if (c != null) {
                DefaultTableModel model = (DefaultTableModel) tblClients.getModel();
                model.setRowCount(0);
                model = (DefaultTableModel) tblClients.getModel();

                Object[] data = new Object[7];

                data[0] = c.getCin();
                data[1] = c.getNom();
                data[2] = c.getPrenom();
                data[3] = c.getDateNaiss();
                data[4] = c.getNumTelephone();
                data[5] = c.getAdresse();
                data[6] = c.getSexe();

                model.addRow(data);

            } else {

                DefaultTableModel model = (DefaultTableModel) tblClients.getModel();
                model.setRowCount(0);

            }
        } else {
            afficherClients();
        }

    }//GEN-LAST:event_txtSearchKeyReleased


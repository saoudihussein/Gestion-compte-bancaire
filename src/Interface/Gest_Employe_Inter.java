    private void btnSuppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuppActionPerformed

        JFrame frame = new JFrame("Message");

        int rep = JOptionPane.showConfirmDialog(null, "Voulez-vous vraiment supprimer " + txtNom.getText() + " " + txtPrenom.getText(), "", JOptionPane.YES_NO_CANCEL_OPTION);

        if (rep == 0) {
            Employe employe = new Employe(txtCin.getText(), 1, txtNom.getText().toUpperCase(), txtPrenom.getText(), new java.sql.Date(12), txtTelphon.getText(), txtAdresse.getText(), buttonGroup1.getSelection().getActionCommand());

            Gest_Employe gc = new Gest_Employe();
            
            if (gc.SupprimerEmploye(employe)) {
                afficheremployes();

                txtCin.setText("");
                txtNom.setText("");
                txtPrenom.setText("");
                txtTelphon.setText("");
                txtAdresse.setText("");
                txtCin.setEnabled(true);
                btnSupp.setEnabled(false);
                btnMod.setEnabled(false);

                JOptionPane.showMessageDialog(frame, employe.getNom() + " " + employe.getPrenom() + " est supprimé", "Succés", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(frame, "Probleme de suppression", "Erreur", JOptionPane.ERROR_MESSAGE);
            }

        }


    }//GEN-LAST:event_btnSuppActionPerformed


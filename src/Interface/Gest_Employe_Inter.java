    private void tblEmployesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmployesMouseClicked
        btnSupp.setEnabled(true);
        btnMod.setEnabled(true);

        DefaultTableModel model = (DefaultTableModel) tblEmployes.getModel();

        Date date;
        try {
            date = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(tblEmployes.getSelectedRow(), 4).toString());
            dteNaiss.setDate(date);
        } catch (ParseException ex) {
            Logger.getLogger(Gest_Employe_Inter.class.getName()).log(Level.SEVERE, null, ex);
        }

        txtCin.setEnabled(false);

        txtCin.setText(model.getValueAt(tblEmployes.getSelectedRow(), 0).toString());
        txtNom.setText(model.getValueAt(tblEmployes.getSelectedRow(), 2).toString());
        txtPrenom.setText(model.getValueAt(tblEmployes.getSelectedRow(), 3).toString());
        txtTelphon.setText(model.getValueAt(tblEmployes.getSelectedRow(), 5).toString());
        txtAdresse.setText(model.getValueAt(tblEmployes.getSelectedRow(), 6).toString());

    }//GEN-LAST:event_tblEmployesMouseClicked

    private void btnAjoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjoutActionPerformed

        JFrame frame = new JFrame("Message");
        if (txtCin.getText().length() == 8 || txtCin.getText().equals("") || txtNom.getText().equals("") || txtPrenom.getText().equals("") || txtTelphon.getText().equals("") || txtAdresse.getText().equals("")) {

            Employe employe = new Employe(txtCin.getText(), txtNom.getText().toUpperCase(), txtPrenom.getText(), dteNaiss.getDate(), txtTelphon.getText(),
                    txtAdresse.getText(), buttonGroup1.getSelection().getActionCommand(), txtNom.getText().toUpperCase().charAt(0) + "" + txtPrenom.getText().toUpperCase().charAt(0),
                    txtNom.getText().toUpperCase().charAt(0) + "" + txtPrenom.getText().toUpperCase().charAt(0) + "_" + txtCin.getText());

            Gest_Employe gc = new Gest_Employe();
            if (gc.AjouterEmploye(employe)) {
                DefaultTableModel model = (DefaultTableModel) tblEmployes.getModel();
                model.setRowCount(0);

                afficheremployes();

                txtCin.setText("");
                txtNom.setText("");
                txtPrenom.setText("");
                txtTelphon.setText("");
                txtAdresse.setText("");
                dteNaiss.setDate(null);

                JOptionPane.showMessageDialog(frame, employe.getNom() + " " + employe.getPrenom() + " est ajouté", "Succés", JOptionPane.INFORMATION_MESSAGE);

            } else {
                JOptionPane.showMessageDialog(frame, "Probleme d'ajout", "Erreur", JOptionPane.ERROR_MESSAGE);
            }

        } else {

            JOptionPane.showMessageDialog(frame, "Les champs invalide", "Erreur ", JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_btnAjoutActionPerformed

    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        if (!txtSearch.getText().equals("")) {

            Gest_Employe gc = new Gest_Employe();
            Employe c = gc.Afficheemploye(txtSearch.getText());

            if (c != null) {
                DefaultTableModel model = (DefaultTableModel) tblEmployes.getModel();
                model.setRowCount(0);
                model = (DefaultTableModel) tblEmployes.getModel();

                Object[] data = new Object[8];

                data[0] = c.getCin();
                data[1] = c.getCode();
                data[2] = c.getNom();
                data[3] = c.getPrenom();
                data[4] = c.getDateNaiss();
                data[5] = c.getNumTelephone();
                data[6] = c.getAdresse();
                data[7] = c.getSexe();

                model.addRow(data);

            } else {

                DefaultTableModel model = (DefaultTableModel) tblEmployes.getModel();
                model.setRowCount(0);

            }
        } else {
            afficheremployes();
        }
    }//GEN-LAST:event_txtSearchKeyReleased


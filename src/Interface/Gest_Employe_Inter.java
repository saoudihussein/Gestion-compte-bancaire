    public void afficheremployes() {
        Gest_Employe gc = new Gest_Employe();
        ArrayList<Employe> c = gc.AfficheEmployes();

        DefaultTableModel model = (DefaultTableModel) tblEmployes.getModel();

        Object[] data = new Object[8];

        model.setRowCount(0);
        for (int i = 0; i < c.size(); i++) {

            data[0] = c.get(i).getCin();
            data[1] = c.get(i).getCode();
            data[2] = c.get(i).getNom();
            data[3] = c.get(i).getPrenom();
            data[4] = c.get(i).getDateNaiss();
            data[5] = c.get(i).getNumTelephone();
            data[6] = c.get(i).getAdresse();
            data[7] = c.get(i).getSexe();

            model.addRow(data);
        }

        lblNbEmploye.setText("Nombre des employés : " + c.size());

    }

